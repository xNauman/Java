package rawobjectsPriv;

import java.awt.Point;
import java.util.Vector;
import rawobjects.NavRequest;

public class BusPath {
	
	private NavRequest vNavRequest;
	private Vector<Point> vBusPoints;
	 
	public NavRequest getNavRequest() {
		return vNavRequest;
	}

	public void setNavRequest(NavRequest pNavRequest) {
		this.vNavRequest = pNavRequest;
	}

	public Vector<Point> getBusPoints() {
		return vBusPoints;
	}

	public void setBusPoints(Vector<Point> vBusPoints) {
		this.vBusPoints = vBusPoints;
	}

	
	public BusPath(NavRequest pNavRequest, Vector<Point> pBusPoints)
	{
		this.vNavRequest = pNavRequest;
		this.vBusPoints = pBusPoints;
	}

}
