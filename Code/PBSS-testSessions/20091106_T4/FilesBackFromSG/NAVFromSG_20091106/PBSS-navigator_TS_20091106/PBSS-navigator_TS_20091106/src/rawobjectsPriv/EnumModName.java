package rawobjectsPriv;

public enum EnumModName {
	CONTROL_CENTRE ("Control Centre", "CC"),
	CME("City Map Editor", "CME"),
	SETUP("Setup", "SU"),
	DB("Database", "DB"),
	TRACKER ("Tracker", "TKR"),
	NAVIGATOR ("Navigator", "NAV"),
	SCHEDULER ("Scheduler", "SCH");

	private String mModName= "";
	private String mModAbbrev= "";
	
	EnumModName(String pModName, String pModAbbrev){
		this.mModName = pModName;
		this.mModAbbrev = pModAbbrev;
	}
		
	public String getModName(){
		return this.mModName;
	}
	
	public String getModAbbrev(){
		return this.mModAbbrev;
	}
}
