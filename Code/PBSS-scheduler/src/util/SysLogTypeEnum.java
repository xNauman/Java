package util;

public enum SysLogTypeEnum {
	INFORMATION (1),
	WARNING (2),
	ERROR (3),
	FATAL (4),
	SECURITY_OK (5),
	SECURITY_FAILURE (6);
	
	private final int mEventTypeID;
	
	SysLogTypeEnum(int pEventTypeID){
		this.mEventTypeID = pEventTypeID;
	}
	
	public int getEventTypeID(){
		return this.mEventTypeID;
	}
}
